package cnpm.QuanLyNhanKhau.model;

public class ModelLePhi {
	private int giaThueNgay;
	private int giaThueBan;
	private int giaThueGhe;
	private int giaThueLoa;
	private int giaThueDai;
	private int giaThueManHinh;
	private int giaThueDen;
	
	public ModelLePhi(int giaThueNgay, int giaThueBan, int giaThueGhe, int giaThueLoa, int giaThueDai,
			int giaThueManHinh, int giaThueDen) {
		this.giaThueNgay    = giaThueNgay;
		this.giaThueBan     = giaThueBan;
		this.giaThueGhe     = giaThueGhe;
		this.giaThueLoa     = giaThueLoa;
		this.giaThueDai     = giaThueDai;
		this.giaThueManHinh = giaThueManHinh;
		this.giaThueDen     = giaThueDen;
	}

	public int getGiaThueNgay() {
		return giaThueNgay;
	}

	public void setGiaThueNgay(int giaThueNgay) {
		this.giaThueNgay = giaThueNgay;
	}

	public int getGiaThueBan() {
		return giaThueBan;
	}

	public void setGiaThueBan(int giaThueBan) {
		this.giaThueBan = giaThueBan;
	}

	public int getGiaThueGhe() {
		return giaThueGhe;
	}

	public void setGiaThueGhe(int giaThueGhe) {
		this.giaThueGhe = giaThueGhe;
	}

	public int getGiaThueLoa() {
		return giaThueLoa;
	}

	public void setGiaThueLoa(int giaThueLoa) {
		this.giaThueLoa = giaThueLoa;
	}

	public int getGiaThueDai() {
		return giaThueDai;
	}

	public void setGiaThueDai(int giaThueDai) {
		this.giaThueDai = giaThueDai;
	}

	public int getGiaThueManHinh() {
		return giaThueManHinh;
	}

	public void setGiaThueManHinh(int giaThueManHinh) {
		this.giaThueManHinh = giaThueManHinh;
	}

	public int getGiaThueDen() {
		return giaThueDen;
	}

	public void setGiaThueDen(int giaThueDen) {
		this.giaThueDen = giaThueDen;
	}
	

}
